namespace RockPaperScissors // Define um namespace para organizar o código relacionado ao jogo Rock Paper Scissors.
{
    /// <summary>
    /// Enumeração Choice representa as possíveis escolhas no jogo Rock Paper Scissors.
    /// </summary>
    public enum Choice
    {
        /// <summary>
        /// Representa a escolha Rock (Pedra).
        /// </summary>
        Rock = 1,     // Atribui o valor 1 à escolha Rock.

        /// <summary>
        /// Representa a escolha Paper (Papel).
        /// </summary>
        Paper = 2,    // Atribui o valor 2 à escolha Paper.

        /// <summary>
        /// Representa a escolha Scissors (Tesoura).
        /// </summary>
        Scissors = 3  // Atribui o valor 3 à escolha Scissors.
    }
}
